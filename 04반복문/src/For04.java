

//예제 : for반복문으로 2단 구구단 출력하기
public class For04 {

	public static void main(String[] args) {
		
		System.out.println("<<-------2단-------->");
		
		int i;
		
		for(i=1; i<=9; i++) {
			
			System.out.println("2 * " + i + " = " + (2*i));
					
		}
		/*
		 	출력결과
				<<-------2단-------->
				2 * 1 = 2
				2 * 2 = 4
				2 * 3 = 6
				2 * 4 = 8
				2 * 5 = 10
				2 * 6 = 12
				2 * 7 = 14
				2 * 8 = 16
				2 * 9 = 18	
		 */
		

	}

}
